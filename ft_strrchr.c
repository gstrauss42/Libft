#include "libft.h"

char *ft_strrchr(char *s, int c)
{
	int i;
	char cc, *r;
	i = 0;
	r = 0;
	cc = (char) c;
	while(s[i] != '\0')
		i++;
	while(i >= 0)
	{
		if(s[i] == cc)
			return(&s[i]);
		i--;
	}
	return(NULL);
}
