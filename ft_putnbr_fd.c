#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char * a;
	int i;
	i = 0;
	a = ft_itoa(n);
	while(a[i] != '\0')
	{
		write(fd, &a, 1);
		i++;
	}
	return;
}
