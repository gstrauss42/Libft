#include "libft.h"

void	sa(int **stack)
{
	int tmp;

	if (ft_isdigit(stack[1][0]))
	{
		tmp = stack[0][0];
		stack[0][0] = stack[1][0];
		stack[1][0] = tmp;
	}
}
