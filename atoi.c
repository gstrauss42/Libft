int		ft_atoi(char *a)
{
	int i;
	int	b;
	int	ret;

	i = 0;
	while (a[i] != '\0')
	{
		b = a[i] - 48;
		if (i == 0)
			ret = b;
		else
			ret = b + ret * 10;
		i++;
	}
	return (ret);
}
